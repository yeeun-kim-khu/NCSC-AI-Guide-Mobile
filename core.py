@st.cache_data(ttl=3600, show_spinner=False)
def classify_intent_with_llm(text: str) -> str:
    """gpt-4o-mini로 빠르게 의도 분류. 반환: 'basic' or 'llm_agent'

    명확한 키워드로 판단 안 되는 애매한 질문에만 호출됨 (약 0.3~0.5초).
    같은 문장은 1시간 캐시.
    """
    try:
        from openai import OpenAI
        client = OpenAI()
        resp = client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {"role": "system", "content": (
                    "국립어린이과학관 안내 챗봇의 의도 분류기입니다.\n"
                    "사용자 메시지를 아래 두 가지 중 하나로만 분류하세요.\n"
                    "basic_faq: 운영시간·관람료·주차·예약·오시는길·층별 안내·프로그램 일정·시설 등 정형화된 안내 요청\n"
                    "llm_agent: 과학 원리 질문·자유로운 대화·감상·칭찬·복잡한 추천·전시물 설명·기타 모든 경우\n"
                    "반드시 basic_faq 또는 llm_agent 중 하나만 답하세요."
                )},
                {"role": "user", "content": text}
            ],
            max_tokens=10,
            temperature=0,
        )
        result = resp.choices[0].message.content.strip().lower()
        return "basic" if "basic_faq" in result else "llm_agent"
    except Exception:
        return "llm_agent"


