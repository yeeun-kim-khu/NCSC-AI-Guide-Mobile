def load_csv_data():
    """CSV files from data directory - real-time loading"""
    def load_csv_safe(path: str) -> pd.DataFrame:
        for enc in ("utf-8-sig", "utf-8", "cp949", "euc-kr"):
            try:
                return pd.read_csv(path, encoding=enc)
            except Exception:
                continue
        return pd.read_csv(path)

    docs = []
